package LogicHamburgesa;

public class Basked {
	
	private long size;
	public static final long DEFAULT_SIZE=100;

	public Basked() {
		this.size=DEFAULT_SIZE;
	}
	

	public Basked(long size) {
		super();
		this.size = size;
	}


	public long getSize() {
		return size;
	}

	public void setSize(long size) {
		this.size = size;
	}
	

}
